package BasisStudy;

/**
 * 关系运算符/比较运算符
 * ==、!=、>、>=、<=、<  返回的类型都是boolean类型的 true  /  false
 */
public class Demo17 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 10;
        System.out.println(a==b);
        System.out.println(a==c);
        System.out.println(a!=b);
    }
}
