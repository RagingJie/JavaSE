package ExecutionFlow;

/**
 * 循环结构：for
 *  语法：
 *      for(初始化语句;条件判断语句;条件控制语句){
 *          循环体语句;
 *      }
 */
public class OrderDemo04_For {
    public static void main(String[] args) {
        //需求：打印5次 hello world
        for (int i = 0; i<5; i++){
            System.out.println("hello world");
        }
    }
}
